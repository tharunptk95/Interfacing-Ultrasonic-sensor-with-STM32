
/* Distance Measurement using Ultrasonic sensor  */


#include "main.h"
#include <string.h>
#include <stdio.h>

#define usTIM	TIM4

TIM_HandleTypeDef htim4;

UART_HandleTypeDef huart2;

#define TRIG_GPIO_Port    GPIOD
#define TRIG_Pin		 GPIO_PIN_0
#define ECHO_GPIO_Port    GPIOD
#define ECHO_Pin 		 GPIO_PIN_0


void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_TIM4_Init(void);
static void MX_USART2_UART_Init(void);

void usDelay(uint32_t uSec);
//Speed of sound in cm/usec
const float speedOfSound = 0.0343/2;
float distance;

char uartBuf[100];

int main(void)
{

	uint32_t numTicks = 0;

  HAL_Init();


  SystemClock_Config();


  MX_GPIO_Init();
  MX_TIM4_Init();
  MX_USART2_UART_Init();

  while (1)
  {
		//Step 1 Trigger setting low
		HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_RESET);
		usDelay(3);
		//Tigger output sends soundwave 10usec
		HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_SET);
		usDelay(10);

		HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_RESET);

		//Step 2 Wait upto Echo pin reaches High
		while(HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_RESET);

		//Step 3 Measuring echo pulse width in usec
		numTicks = 0;
		while(HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_SET)
		{
			numTicks++;
			usDelay(2); //2.8usec
		};

		//Step 4 Distance measured in cm
		distance = (numTicks + 0.0f)*2.8*speedOfSound;

		//Step 5 Print distance via uart
		printf(uartBuf, "Distance (cm)  = %.1f\r\n", distance);
		HAL_UART_Transmit(&huart2, (uint8_t *)uartBuf, strlen(uartBuf), 100);

		HAL_Delay(1000);

  }

}


